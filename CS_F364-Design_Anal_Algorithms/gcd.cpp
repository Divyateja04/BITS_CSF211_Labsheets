#include <bits/stdc++.h>
using namespace std;

int main()
{
    freopen("gcd.txt", "r", stdin);
    int counter;
    cin >> counter;

    for (int m = 0; m < counter; m++)
    {
        cout << "----------- TEST CASE " << m + 1 << " -----------" << endl;

        cout << "--------- END TEST CASE "
             << " ---------" << endl;
    }
}